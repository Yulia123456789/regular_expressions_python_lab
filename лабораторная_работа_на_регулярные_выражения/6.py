import re

date = [
    "29/02/2024",
    "28/02/2023",
    "31/04/2024",  # в апреле только 30 дней
    "30/02/2024",  # в феврале только 29 или 28
    "31/12/2025",
    "29/02/2023",  # Невисокосный год, февраль
    "29/02/8424",
    "29/02/9996",
    "29/02/2800",
    "29/02/9998",
    "29/02/1600",
    "31/11/2020 ",
    "29/02/2100",
    "01/01/2005",
    "1/1/2033"
]

reg = r'^(?:(?:0[1-9]|[12][0-9]|3[01])/(?:0[13578]|1[02])|(?:0[1-9]|[12][0-9]|30)/(?:0[469]|11)|(?:0[1-9]|1[0-9]|2[0-8])/02)/(?:1[6-9][0-9]{2}|[2-9][0-9]{3})$|^(29/02/(?:(?:1[6-9]|[2-9][0-9])(?:0[48]|[2468][048]|[13579][26])|(?:16|[2468][048]|[3579][26])00))$'

for i in date:
    if re.search(reg, i) != None:
        print(f'Правильная дата: {i}')

    else:
        print(f'Неправильная дата: {i}')