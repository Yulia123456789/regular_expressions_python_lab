
import re

exp = ["((3+6)+8)", "((9*7)-(5+4))", "((3+(7))", "((8*7)-9", "1+(6*9)-4", "(8*6)+(3+8)", "(5*6(2-5)+2-8))"]

reg = r"[^()]*\([^()]*\)[^()]*"
a = {'правильное выражение' : [], 'неправильное выражение' : []}
c = 0

for i in exp:
    while re.search(reg, i):
        i = re.sub(reg, '', i)
    if i == '':
        a['правильное выражение'].append(exp[c])
    else:
        a['неправильное выражение'].append(exp[c])
    c += 1

print(a)